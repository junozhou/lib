||符号隔开，表示依次执行命令，只要有一个执行成功，则退出执行。

[root@localhost /]# date||date||date
2019年 05月 03日 星期五 09:09:24 CST
[root@localhost /]# data||date||ls
-bash: data: 未找到命令
2019年 05月 03日 星期五 09:09:42 CST